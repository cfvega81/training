//Operadores matemáticos
console.log(5 + 3); // Suma 8
console.log(5 - 3); // Resta 2
console.log(5 * 3); // Multiplicacion 15
console.log(5 / 3); // Division 1.6666666666666667
console.log(5 % 3); // Modulo 2
console.log(5 ** 3); // Potencia 125

// Operadores de incremento y decremento
let a2 = 5;
let b3 = a2++;
console.log(a2); // 6
console.log(b3); // 5

let a3 = 5;
let b4 = ++a3;
console.log(a3); // 6
console.log(b4); // 6

let a4 = 5;
let b5 = a4--;
console.log(a4); // 4
console.log(b5); // 5

let a5 = 5;
let b6 = --a5;
console.log(a5); // 4
console.log(b6); // 4

// Operadores de asignación
let a6 = 5;
a6 += 3; // a = a + 3
console.log(a6); // 8

let b7 = 5;
b7 -= 3; // b = b - 3
console.log(b7); // 2

let c2 = 5;
c2 *= 3; // c = c * 3
console.log(c2); // 15

let d2 = 5;
d2 /= 3; // d = d / 3
console.log(d2); // 1.6666666666666667

let e2 = 5;
e2 %= 3; // e = e % 3
console.log(e2); // 2

let f2 = 5;
f2 **= 3; // f = f ** 3
console.log(f2); // 125

// Operadores negativo
let a7 = 5;
let b8 = -a7;
console.log(b8); // -5

let a8 = -5;
let b9 = -a8;
console.log(b9); // 5






